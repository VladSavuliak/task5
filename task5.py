def pirates():
    m = int(input("Введіть кількість монет, які хочуть розділити пірати: "))
    a = int(input("Введіть кількість монет, скільки дістанеться наймолодшому пірату: "))
    n = 0
    while m > 0:
        n += 1
        m -= a
        a += 1
        if a * 2 == m:
            m -= a * 2
            n += 1

    return f"Всього в команді {n} піратів."

print(pirates())




